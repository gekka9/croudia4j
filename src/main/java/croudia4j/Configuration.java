package croudia4j;

public class Configuration {
  private String key;
  private String secret;
  public Configuration(){
    
  }
  public String getKey() {
    return key;
  }
  public void setKey(String key) {
    this.key = key;
  }
  public String getSecret() {
    return secret;
  }
  public void setSecret(String secret) {
    this.secret = secret;
  }
}
